//when the next warp dest is an in-level teleport, stop mario from teleporting twice
void usamune_prevent_double_warp() {
  if (uStopDoubleWarp) {
    uStopDoubleWarp = FALSE;
    sJustTeleported = TRUE;
  }
}

u8 find_next_valid_warp(u8 currWarpId, u8 field, s8 dir) {
  s16 nextId = currWarpId;
  switch (field) {
  case 0:
    nextId += dir;
    if (nextId > 167) {
      nextId = 0;
    }
    else if (nextId < 0) {
      nextId = 167;
    }
    break;
  case 1:
    while (uWarpDestTable[nextId].areaIdx == uWarpDestTable[currWarpId].areaIdx) {
      nextId += dir;
      if (nextId > 167) {
	nextId = 0;
	break;
      }
      else if (nextId < 0) {
	nextId = 167;
	break;
      }
      if (uWarpDestTable[nextId].levelNum != uWarpDestTable[currWarpId].levelNum) {
	break;
      }
    }
    break;
  case 2:
    while (uWarpDestTable[nextId].levelNum == uWarpDestTable[currWarpId].levelNum) {
      nextId += dir;
      if (nextId > 167) {
	nextId = 0;
	break;
      }
      else if (nextId < 0) {
	nextId = 167;
	break;
      }
    }
    break;
  }

  return (u8)nextId;
}
